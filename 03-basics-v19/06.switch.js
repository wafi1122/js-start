// switch statement example

const month = 3;

switch (month) {
    case 1:
        console.log('janvary')
        break;
    case 2:
        console.log('feb')
        break;
    case 3:
        console.log('march')
        break;
    case 4:
        console.log('april')
        break;

    default:
        console.log("month is not matched")
        break;
}